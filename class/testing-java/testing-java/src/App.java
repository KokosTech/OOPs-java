public class App {
    public static void main(String[] args) {
        Integer a = 1;
        Long b = 2L;
        System.out.println(a + b);
        Float c = 3.0f;
        Double d = 4.0d;
        Boolean e = true;
        System.out.printf("%f, %f, %b\n", c, d, e);
        Character f = 'f';
        String g = "g";
        System.out.println(f + g);
    }
}
